package week_3;

public class Forloop6 {

	public static void main(String[] args) {
		
		/*
		 * Print hey Ilhan after 5 using for loop
		 * 1 2 3 4 5 hey Ilhan 6 7 8 9 .... 20
		 */
		
		for(int s =1; s<=20; s++){
			System.out.println(s);
			if(s==5){
				System.out.println("hey ilhan");
			}
		}

	}

}

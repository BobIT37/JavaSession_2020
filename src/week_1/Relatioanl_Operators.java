package week_1;

public class Relatioanl_Operators {

	public static void main(String[] args) {
		
		//RO is used to compares two values
		
		/*
		 * ==
		 * >
		 * <
		 * >=
		 * <=
		 * !
		 * !=
		 */
		
		int a = 10;
		int b = 25;
		int c = 25;
		
		// > greater than 
		System.out.println("Greater than");
		System.out.println("a > b= "+ (a > b));
		System.out.println("b > a= "+ (b > a));
		System.out.println("c > b= "+ (c > b));
		System.out.println("");
		
		
		// greater than equal
		System.out.println("Greater than equal");
		System.out.println("a >= b= "+ (a >= b));
		System.out.println("b >= a= "+ (b >= a));
		System.out.println("c >= b= "+ (c >= b));
		System.out.println("");
		
		//less than
		System.out.println("less than");
		System.out.println("a <= b= "+ (a <= b));
		System.out.println("b <= a= "+ (b <= a));
		System.out.println("c <= b= "+ (c <= b));
		System.out.println("");
		
		// equal
		System.out.println("Equal");
		System.out.println("a == b= "+ (a == b));
		System.out.println("b == a= "+ (b == a));
		System.out.println("c == b= "+ (c == b));
		System.out.println("");
		
		//Not equal to
		System.out.println("Equal");
		System.out.println("a != b= "+ (a != b));
	    System.out.println("b != a= "+ (b != a));
		System.out.println("c != b= "+ (c != b));
		System.out.println("");
		

	}

}

package week_1;

public class CharOperators {

	public static void main(String[] args) {
		
		// Char is chracters
		
		char letter = 'A';
		System.out.println(letter);
		int ascii = letter;
		System.out.println(ascii); //UTF-8
		
		char myChar = 'B';
		System.out.println(myChar);
		++myChar;
		System.out.println(myChar);
		
		char ch = 65;
		System.out.println(ch);

	}

}
